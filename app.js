require('dotenv').config();
const createError = require('http-errors');
const express = require('express');
const path = require('path');
const cookieParser = require('cookie-parser');
const logger = require('morgan');
const mongoose = require('mongoose');
const cors = require('cors');

const indexRouter = require('./routes/index');
const usersRouter = require('./routes/users');
const commentRouter = require('./routes/comments');
const blogsRouter = require('./routes/blogs');
const config = require('./middleware/config');

// Connection begins
mongoose.connect(process.env.DATABASE_URL, config.MONGO_OPTIONS);
const db = mongoose.connection;
db.on('error', console.error.bind(console, 'Connection Error, Please check your MongoDB!!'));
db.once('open', () => {
  console.log('Connection to Mongo server successful!!');
});
// Connection ends here.Connection error to MYSQL DB Server. Check if Server is running

const app = express();

// view engine setup
app.set('views', path.join(__dirname, 'views'));
app.set('view engine', 'pug');

app.use(cors());
app.use(logger('dev'));
app.use(express.json());
app.use(express.urlencoded({ extended: false }));
app.use(cookieParser());
app.use(express.static(path.join(__dirname, 'public')));

app.use('/', indexRouter);
app.use('/users', usersRouter);
app.use('/blogs', blogsRouter);
app.use('/comments', commentRouter);

// catch 404 and forward to error handler
app.use(function(req, res, next) {
  next(createError(404));
});

// error handler
app.use(function(err, req, res, next) {
  // set locals, only providing error in development
  res.locals.message = err.message;
  res.locals.error = req.app.get('env') === 'development' ? err : {};

  // render the error page
  res.status(err.status || 500);
  res.render('error');
});

module.exports = app;
